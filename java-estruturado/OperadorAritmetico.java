public class OperadorAritmetico{

    public static void main(String[] args) {
        
        int soma = 2 + 10;
        int subtracao = 6 - 10;
        int multiplicao = 8 * 3;
        int divisao = 8 / 2;
        int resto = 7 % 2;

        System.out.println(soma);
        System.out.println(subtracao);
        System.out.println(multiplicao);
        System.out.println(divisao);
        System.out.println(resto);
    }
}

